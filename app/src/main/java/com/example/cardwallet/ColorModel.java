package com.example.cardwallet;

public class ColorModel {

    public ColorModel(String color, boolean t) {
        this.color = color;
        this.t = t;
    }

    private String color;
    private boolean t;

    public String getColor() {
        return color;
    }

    public void setColor(String color) {
        this.color = color;
    }

    public boolean isT() {
        return t;
    }

    public void setT(boolean t) {
        this.t = t;
    }



}
